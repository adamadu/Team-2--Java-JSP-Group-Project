    //Adam- Function to check if the provided internal frame is already open
    //If it is, it will set it to selected.
    public static boolean checkIfFrameAlreadyOpen(Class c)
    {
        for(javax.swing.JInternalFrame frame : jDesktopPane2.getAllFrames())
        {
            if(c.isInstance(frame))
            {
                try {                  
                    if(frame.isIcon())
                        frame.setIcon(false);
                    
                    frame.setSelected(true);
                    frame.setLocation(0, 0);
                } catch (PropertyVetoException ex) {
                    Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
                }
                return true;
            }
        }
        return false;
    }
    
